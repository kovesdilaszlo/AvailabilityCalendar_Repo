namespace AvailabilityCalendar.Web.ViewModels.Calendar;

/// <summary>
/// Defines the supported calendar view types.
/// </summary>
public enum CalendarViewType
{
    /// <summary>
    /// Single day view.
    /// </summary>
    Day = 0,

    /// <summary>
    /// Seven day week view.
    /// </summary>
    Week = 1,

    /// <summary>
    /// Month grid view.
    /// </summary>
    Month = 2
}